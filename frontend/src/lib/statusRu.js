/** Соответствие статусов API и русских подписей */
export const appStatusLabel = (s) =>
  ({
    sent: 'Отправлено',
    skipped: 'Пропущено',
    error: 'Ошибка',
    blacklisted: 'В блэклисте',
  })[s] || s

export const appStatusTitle = (s) =>
  ({
    sent: 'Отклик сохранён как отправленный (через расширение).',
    skipped: 'Вакансия пропущена (уже откликались, прямой контакт без отклика, тест и т.д.).',
    error: 'При обработке вакансии произошла ошибка (сеть, лимиты и т.д.).',
    blacklisted:
      'Вакансия в чёрном списке: автоматические отклики по ней не выполняются (повторные ошибки UI и т.п.).',
  })[s] || `Статус от сервера: ${s}`

